    <script>
        // JavaScript
        function generateQR() {
            var tanggalSurat = document.getElementById("tanggalSurat").value;
            var perihal = document.getElementById("perihal").value;
            var namaPemberi = document.getElementById("namaPemberi").value;

            var qrText = Tanggal Surat: ${tanggalSurat}\nPerihal: ${perihal}\nNama Pemberi: ${namaPemberi};

            var qr = new QRious({
                element: document.getElementById("qrcode"),
                value: qrText,
                size: 200
            });
        }
    </script>
